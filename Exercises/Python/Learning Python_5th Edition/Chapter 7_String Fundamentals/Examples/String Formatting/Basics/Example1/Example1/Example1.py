# Format expression
print("That is %d %s bird!" %(1, "dead"))

exclamation = "Ni"
# String substitution
print("The knights who say %s!" %exclamation)

# Type-specific substitution
print("%d %s %g you" %(1, "spam", 4.0))

# All types match a %s target
print("%s --- %s -- %s" %(42, 3.13159, [1, 2, 3]))
