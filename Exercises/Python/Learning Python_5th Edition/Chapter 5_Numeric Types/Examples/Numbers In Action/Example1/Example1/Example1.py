# Name created: not declared ahead of time
a = 3
b = 4

# Addition(3 + 1), subtraction(3 - 1)
print(a + 1, a - 1)

# Multiplication (4 * 3), division (4 / 2)
print(b * 3, b / 2)

# Modulus(remainder), power (4 ** 2)
print(a % 2, b ** 2)

# Mixed-type conversions
print(2 + 4.0, 2.0 ** b)

# Same as ((4/2) + 3)
print(b/2 + a)

# Same as (4/(2.0 + 3))
print(b / (2.0 + a))