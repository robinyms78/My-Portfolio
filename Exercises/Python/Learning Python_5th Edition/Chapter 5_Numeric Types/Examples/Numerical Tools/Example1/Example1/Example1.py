import math

# Common constants
print(math.pi)
print(math.e)

# sine, cosine, tangent
print(math.sin(2 * math.pi/180))

# Square root
print(math.sqrt(144), math.sqrt(2))

# Exponentiation (power)
print(pow(2, 4), 2 ** 4, 2.0 ** 4.0)

# Absolute value, summation
print(abs(-42.0), sum((1, 2, 3, 4)))

# Minimum, maximum
print(min(3, 1, 2, 4), max(3, 1, 2, 4))